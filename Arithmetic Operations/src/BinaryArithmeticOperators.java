public class BinaryArithmeticOperators {
    public static void main(String[] args) {

        //addition +
        System.out.println(78 + 78);

        //subtraction -
        System.out.println(36 - 33);

        //multiplication *
        System.out.println(36 * 36);

        // integer division /
        System.out.println(78 / 73);

        // remainder %
        System.out.println(23 % 7);

        // more complex expressions:
        System.out.println(7 + 6 * 8 - 3);
        System.out.println((7 + 6) * (8 - 3));
    }
}
